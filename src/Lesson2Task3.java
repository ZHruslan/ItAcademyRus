public class Lesson2Task3 {
    public static void main(String[] args) {
        double areaCircle;
        double circleLength;
        double r = 2.3;
        final double pi = 3.14;
        areaCircle = 2 * pi * r;
        circleLength = r * r * pi;
        System.out.println("Площадь круга = " + areaCircle);
        System.out.println("Длинна окружности = " + circleLength);
    }
}
